# 数据类型的概念：
# 在Python里数据都有各自的对应的类型：

print(45 + 1)   # int整数类型
print(3.1415)  # float类型
print((-1) ** 0.5)  # complex 类型

# 字符串类型：其实就是一段普通的文字
# python里的字符串要求使用一对单引号，或者双引号来包裹

print('今天天气好，处处好风光')

print("56")

#  布尔类型里
print(4 > 3)  # 真 True
print(1 > 5)  # 假 False


# 列表类型
name = ['姚万万','蔡徐坤','李想','郑美水']
print(name)

#  元祖类型
lab = (1,8,2,9,10,0)
print(lab)

#  字典类型
# person = {'name':'jake','age:' = 18,'addr':'CH','sex':'man' }
person = {'name':'jake','age':18,'addr':'CH'}
print(person)

#  集合类型
x = {9,'hello','hi','good',True}
print(x)
