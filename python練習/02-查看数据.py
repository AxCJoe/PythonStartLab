a = 34
b = 'hello'
c = True
d = ['周杰伦','蔡徐坤']

#  使用type内置类可以查看一个变量对应的书库类型

print(type(a))  # <class 'int'> 整数型
print(type(b))  # <class 'str'> 字符串类型
print(type(c))  # <class 'bool'> 布尔型
print(type(d))  # <class 'list'> 列表

#  type(a).print 按回车自动补全成 print(type(a))

print(type(a))
print(type(3.14))  # <class 'float'> 浮点数

# 在python里，变量是没有数据类型的
